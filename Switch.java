public class Switch {
    public static void main(String[] args) {
        int x=1;

        switch (x)
        {
            case 1: System.out.print( "this is 1"); break;
            case 2: System.out.print( "this is 2"); break;
            case 3: System.out.print( "this is 3"); break;
            default: System.out.print( "None of the above ");

        }
    }
}
